import 'package:library_architecture_mvvm_modify/library_architecture_mvvm_modify.dart';
import 'package:meta/meta.dart';
import 'package:test_lamm/model/game_match/game_match.dart';

@immutable
base class ListGameMatch<T extends GameMatch> extends BaseListModel<T> {
  const ListGameMatch(super.listModel);

  @override
  ListGameMatch<T> clone() {
    List<T> newListModel = List.empty(growable: true);
    for (final T itemModel in listModel) {
      newListModel.add(itemModel.clone() as T);
    }
    return ListGameMatch<T>(newListModel);
  }

  @override
  String toString() {
    String strListModel = "\n";
    for (final T itemModel in listModel) {
      strListModel += "$itemModel,\n";
    }
    return "ListGameMatch(listModel: [$strListModel])";
  }
}